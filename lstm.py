# 10. Wrapper để sử dụng mô hình Keras trong GridSearchCV
from sklearn.base import BaseEstimator, RegressorMixin, clone

class KerasRegressorCustom(BaseEstimator, RegressorMixin):
    def __init__(self, units=32, learning_rate=0.001, epochs=150, batch_size=16, verbose=0):
        self.units = units
        self.learning_rate = learning_rate
        self.epochs = epochs
        self.batch_size = batch_size
        self.verbose = verbose
        self.model = None
        self.history = None

    def fit(self, X, y, validation_data=None):
        self.model = create_lstm_model(self.units, self.learning_rate)
        early_stopping = EarlyStopping(monitor='val_loss', patience=10, restore_best_weights=True)

        if validation_data is not None:
            self.history = self.model.fit(
                X, y,
                epochs=self.epochs,
                batch_size=self.batch_size,
                verbose=self.verbose,
                validation_data=validation_data,
                callbacks=[early_stopping]
            )
        else:
            self.history = self.model.fit(
                X, y,
                epochs=self.epochs,
                batch_size=self.batch_size,
                verbose=self.verbose,
                callbacks=[early_stopping]
            )
        return self

    def predict(self, X):
        return self.model.predict(X)

    def score(self, X, y):
        y_pred = self.predict(X)
        return -mean_squared_error(y, y_pred)  # GridSearchCV tìm max nên ta cần dấu trừ

